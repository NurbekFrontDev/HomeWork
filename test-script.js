const random = [1, 2, 3, 4, 5, 6, 7];
function reverseArray(reversed) {
  let left = 0;
  let right = reversed.length - 1;

  while (left < right) {
    let temp = reversed[left];
    reversed[left] = reversed[right];
    reversed[right] = temp;

    left++;
    right--;
  }

  return reversed;
}

console.log(reverseArray(random));

